package variables;

public class Program3 {
    public static void main(String[] args) {
        int a = 5;
        float b = 5.99f;
        char c = 'N';
        boolean d = true;
        String e = "Nidhi Gandhi";

        System.out.println("My integer value is : " + a);
        System.out.println("My Float value is : " + b);
        System.out.println("My char value is : " + c);
        System.out.println("My boolean value is : " + d);
        System.out.println("My String value is : " + e);
    }
}
